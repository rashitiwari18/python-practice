# escape sequence '\n'
print("hello world \n how are u all")

# to quote inside a string we again use escape sequence ' \" '
print("hello world \"it's a lovely day\" \n how are u all")

# multiple values inside print()
print("rashi",20,"female")
print("rashi",20,"female", sep="~")  #separator
print("rashi ",20," female", sep="~",end="\n 2003 \n")  #end with
print("netaji subhas university")